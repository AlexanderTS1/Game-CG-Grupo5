def Traslate(vertices, tx, ty):
        T = [
                [1, 0, tx], 
                [0, 1, ty], 
                [0, 0, 1]
        ]
        result = []
        for item in vertices:
                point = np.dot(T, item)
                result.append(point)
        return result
